public class ReginaldoAraujo {
    String altura;
    String fisionomia;
    String forca;


    public ReginaldoAraujo() {
        this.altura = "alto";
        this.fisionomia = "forte";
        this.forca = "musculoso";
    }

    public String getAltura() {
        return altura;
    }

    public void setAltura(String altura) {
        this.altura = altura;
    }

    public String getFisionomia() {
        return fisionomia;
    }

    public void setFisionomia(String fisionomia) {
        this.fisionomia = fisionomia;
    }

    public String getForca() {
        return forca;
    }

    public void setForca(String forca) {
        this.forca = forca;
    }
}

